<script setup>
import { ref, computed, onMounted } from 'vue'
import { ElMessage, ElMessageBox } from 'element-plus'
import {
  Plus, ArrowDown, MagicStick, EditPen
} from '@element-plus/icons-vue'
import { getTeacherCourses } from '@/api/courses'
import {
  getTeacherExamPapers,
  getExamPaperById,
  createExamPaper,
  updateExamPaper,
  deleteExamPaper,
  publishExamPaper,
  autoGenerateExamPaper,
  getExamPaperStatuses,
  getCreationMethods
} from '@/api/examPapers'
import { getTeacherQuestions, getQuestionTypes } from '@/api/questions'

const activeTab = ref('list')
const courses = ref([])
const examPapers = ref([])
const questionTypes = ref([])
const paperStatuses = ref([])
const creationMethods = ref([])
const loading = ref(false)

const filterCourseId = ref(null)
const filterStatus = ref(null)

const filteredPapers = computed(() => {
  let result = examPapers.value
  if (filterCourseId.value) {
    result = result.filter(p => p.courseId === filterCourseId.value)
  }
  if (filterStatus.value) {
    result = result.filter(p => p.status === filterStatus.value)
  }
  return result
})

const autoGenerateDialogVisible = ref(false)
const generating = ref(false)
const autoConfig = ref({
  courseId: null,
  title: '',
  duration: 120,
  typeConfigs: [
    { type: 'SINGLE_CHOICE', count: 10, scorePerQuestion: 2, difficulty: '' }
  ]
})

const totalAutoQuestions = computed(() => {
  return autoConfig.value.typeConfigs.reduce((sum, c) => sum + (c.count || 0), 0)
})

const totalAutoScore = computed(() => {
  return autoConfig.value.typeConfigs.reduce((sum, c) => sum + (c.count || 0) * (c.scorePerQuestion || 0), 0)
})

const manualCreateDialogVisible = ref(false)
const isEditing = ref(false)
const currentPaperId = ref(null)
const saving = ref(false)
const paperForm = ref({
  title: '',
  description: '',
  courseId: null,
  duration: 120,
  totalScore: 0
})

const selectedQuestions = ref([])
const selectedTotalScore = computed(() => {
  return selectedQuestions.value.reduce((sum, q) => sum + (q.score || 0), 0)
})

const questionSelectorVisible = ref(false)
const selectorLoading = ref(false)
const selectorQuestions = ref([])
const selectorSelected = ref([])
const selectorFilter = ref({
  courseId: null,
  type: null,
  difficulty: null
})

const detailDialogVisible = ref(false)
const currentPaper = ref(null)

const getStatusType = (status) => {
  const map = {
    DRAFT: 'info',
    PUBLISHED: 'success',
    ARCHIVED: 'warning'
  }
  return map[status] || 'info'
}

const getDifficultyType = (difficulty) => {
  const map = {
    '简单': 'success',
    '中等': 'warning',
    '困难': 'danger'
  }
  return map[difficulty] || 'info'
}

const fetchCourses = async () => {
  try {
    const res = await getTeacherCourses()
    courses.value = res.data
  } catch (error) {
    console.error(error)
  }
}

const fetchQuestionTypes = async () => {
  try {
    const res = await getQuestionTypes()
    questionTypes.value = res.data
  } catch (error) {
    console.error(error)
  }
}

const fetchPaperStatuses = async () => {
  try {
    const res = await getExamPaperStatuses()
    paperStatuses.value = res.data
  } catch (error) {
    console.error(error)
  }
}

const fetchExamPapers = async () => {
  loading.value = true
  try {
    const res = await getTeacherExamPapers(filterCourseId.value)
    examPapers.value = res.data
  } catch (error) {
    console.error(error)
    ElMessage.error('获取试卷列表失败')
  } finally {
    loading.value = false
  }
}

const handleCreateCommand = (command) => {
  if (command === 'auto') {
    openAutoGenerateDialog()
  } else {
    openManualCreateDialog()
  }
}

const openAutoGenerateDialog = () => {
  autoConfig.value = {
    courseId: null,
    title: '',
    duration: 120,
    typeConfigs: [
      { type: 'SINGLE_CHOICE', count: 10, scorePerQuestion: 2, difficulty: '' }
    ]
  }
  autoGenerateDialogVisible.value = true
}

const addTypeConfig = () => {
  autoConfig.value.typeConfigs.push({
    type: 'SINGLE_CHOICE',
    count: 5,
    scorePerQuestion: 2,
    difficulty: ''
  })
}

const removeTypeConfig = (index) => {
  autoConfig.value.typeConfigs.splice(index, 1)
}

const submitAutoGenerate = async () => {
  if (autoConfig.value.typeConfigs.length === 0) {
    ElMessage.warning('请至少配置一种题目类型')
    return
  }

  const validConfigs = autoConfig.value.typeConfigs.filter(c => c.type && c.count > 0 && c.scorePerQuestion > 0)
  if (validConfigs.length === 0) {
    ElMessage.warning('请完整配置题目类型、数量和分值')
    return
  }

  generating.value = true
  try {
    const config = {
      courseId: autoConfig.value.courseId,
      totalScore: totalAutoScore.value,
      duration: autoConfig.value.duration,
      typeConfigs: validConfigs
    }

    const res = await autoGenerateExamPaper(config)
    
    if (autoConfig.value.title) {
      await updateExamPaper(res.data.id, {
        title: autoConfig.value.title,
        courseId: autoConfig.value.courseId
      })
    }

    ElMessage.success('自动组卷成功')
    autoGenerateDialogVisible.value = false
    fetchExamPapers()
  } catch (error) {
    console.error(error)
    ElMessage.error(error.response?.data?.message || '组卷失败')
  } finally {
    generating.value = false
  }
}

const openManualCreateDialog = () => {
  isEditing.value = false
  currentPaperId.value = null
  paperForm.value = {
    title: '',
    description: '',
    courseId: null,
    duration: 120,
    totalScore: 0
  }
  selectedQuestions.value = []
  manualCreateDialogVisible.value = true
}

const editPaper = async (row) => {
  try {
    const res = await getExamPaperById(row.id)
    currentPaperId.value = row.id
    isEditing.value = true
    paperForm.value = {
      title: res.data.title,
      description: res.data.description || '',
      courseId: res.data.courseId,
      duration: res.data.duration,
      totalScore: res.data.totalScore
    }
    selectedQuestions.value = res.data.questions ? res.data.questions.map((q, index) => ({
      ...q,
      sortOrder: index + 1
    })) : []
    manualCreateDialogVisible.value = true
  } catch (error) {
    console.error(error)
    ElMessage.error('获取试卷详情失败')
  }
}

const openQuestionSelector = () => {
  selectorFilter.value = {
    courseId: paperForm.value.courseId,
    type: null,
    difficulty: null
  }
  selectorQuestions.value = []
  selectorSelected.value = []
  questionSelectorVisible.value = true
  searchQuestions()
}

const searchQuestions = async () => {
  selectorLoading.value = true
  try {
    const res = await getTeacherQuestions(selectorFilter.value.courseId)
    let questions = res.data

    if (selectorFilter.value.type) {
      questions = questions.filter(q => q.type === selectorFilter.value.type)
    }
    if (selectorFilter.value.difficulty) {
      questions = questions.filter(q => q.difficulty === selectorFilter.value.difficulty)
    }

    const selectedIds = new Set(selectedQuestions.value.map(q => q.questionId))
    selectorQuestions.value = questions.filter(q => !selectedIds.has(q.id))
  } catch (error) {
    console.error(error)
    ElMessage.error('获取题目列表失败')
  } finally {
    selectorLoading.value = false
  }
}

const handleSelectorSelectionChange = (selection) => {
  selectorSelected.value = selection
}

const confirmSelectorSelection = () => {
  const newQuestions = selectorSelected.value.map((q, index) => ({
    questionId: q.id,
    id: q.id,
    type: q.type,
    typeName: q.typeName,
    content: q.content,
    options: q.options,
    answer: q.answer,
    explanation: q.explanation,
    difficulty: q.difficulty,
    score: q.score,
    sortOrder: selectedQuestions.value.length + index + 1
  }))
  selectedQuestions.value = [...selectedQuestions.value, ...newQuestions]
  questionSelectorVisible.value = false
}

const removeSelectedQuestion = (index) => {
  selectedQuestions.value.splice(index, 1)
  selectedQuestions.value.forEach((q, i) => {
    q.sortOrder = i + 1
  })
}

const updateSelectedQuestionScore = (index, score) => {
  if (selectedQuestions.value[index]) {
    selectedQuestions.value[index].score = score
  }
}

const submitManualPaper = async () => {
  if (!paperForm.value.title?.trim()) {
    ElMessage.warning('请输入试卷标题')
    return
  }

  saving.value = true
  try {
    const data = {
      title: paperForm.value.title,
      description: paperForm.value.description,
      courseId: paperForm.value.courseId,
      duration: paperForm.value.duration,
      totalScore: selectedTotalScore.value,
      questionCount: selectedQuestions.value.length,
      questions: selectedQuestions.value.map((q, index) => ({
        questionId: q.questionId,
        sortOrder: index + 1,
        score: q.score
      }))
    }

    if (isEditing.value && currentPaperId.value) {
      await updateExamPaper(currentPaperId.value, data)
      ElMessage.success('更新成功')
    } else {
      await createExamPaper(data)
      ElMessage.success('创建成功')
    }

    manualCreateDialogVisible.value = false
    fetchExamPapers()
  } catch (error) {
    console.error(error)
    ElMessage.error(error.response?.data?.message || '操作失败')
  } finally {
    saving.value = false
  }
}

const viewPaper = async (row) => {
  try {
    const res = await getExamPaperById(row.id)
    currentPaper.value = res.data
    detailDialogVisible.value = true
  } catch (error) {
    console.error(error)
    ElMessage.error('获取试卷详情失败')
  }
}

const publishPaper = async (row) => {
  try {
    await ElMessageBox.confirm('确定要发布该试卷吗？发布后将无法修改。', '确认发布', {
      confirmButtonText: '确定',
      cancelButtonText: '取消',
      type: 'warning'
    })

    await publishExamPaper(row.id)
    ElMessage.success('发布成功')
    fetchExamPapers()
  } catch (error) {
    if (error !== 'cancel') {
      console.error(error)
      ElMessage.error(error.response?.data?.message || '发布失败')
    }
  }
}

const deletePaper = async (row) => {
  try {
    await ElMessageBox.confirm('确定要删除该试卷吗？删除后无法恢复。', '确认删除', {
      confirmButtonText: '确定',
      cancelButtonText: '取消',
      type: 'warning'
    })

    await deleteExamPaper(row.id)
    ElMessage.success('删除成功')
    fetchExamPapers()
  } catch (error) {
    if (error !== 'cancel') {
      console.error(error)
      ElMessage.error(error.response?.data?.message || '删除失败')
    }
  }
}

