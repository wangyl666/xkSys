        </el-table-column>
      </el-table>

      <el-empty v-if="selectedQuestions.length === 0" description="暂无题目，请点击上方添加题目" :image-size="80" />

      <template #footer>
        <el-button @click="manualCreateDialogVisible = false">取消</el-button>
        <el-button type="primary" @click="submitManualPaper" :loading="saving">
          保存试卷
        </el-button>
      </template>
    </el-dialog>

    <el-dialog
      v-model="questionSelectorVisible"
      title="选择题目"
      width="1000px"
    >
      <el-card shadow="never" style="margin-bottom: 16px">
        <el-row :gutter="16">
          <el-col :span="6">
            <el-select v-model="selectorFilter.courseId" placeholder="选择课程" clearable style="width: 100%">
              <el-option
                v-for="course in courses"
                :key="course.id"
                :label="course.courseName"
                :value="course.id"
              />
            </el-select>
          </el-col>
          <el-col :span="5">
            <el-select v-model="selectorFilter.type" placeholder="题目类型" clearable style="width: 100%">
              <el-option
                v-for="type in questionTypes"
                :key="type.value"
                :label="type.label"
                :value="type.value"
              />
            </el-select>
          </el-col>
          <el-col :span="5">
            <el-select v-model="selectorFilter.difficulty" placeholder="难度" clearable style="width: 100%">
              <el-option label="简单" value="简单" />
              <el-option label="中等" value="中等" />
              <el-option label="困难" value="困难" />
            </el-select>
          </el-col>
          <el-col :span="4">
            <el-button type="primary" @click="searchQuestions">搜索</el-button>
          </el-col>
        </el-row>
      </el-card>

      <el-table
        :data="selectorQuestions"
        v-loading="selectorLoading"
        stripe
        @selection-change="handleSelectorSelectionChange"
      >
        <el-table-column type="selection" width="55" />
        <el-table-column prop="typeName" label="类型" width="80" />
        <el-table-column prop="content" label="题目内容" min-width="300" show-overflow-tooltip />
        <el-table-column prop="courseName" label="课程" width="100" />
