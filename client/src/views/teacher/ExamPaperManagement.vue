        <el-button type="primary" @click="confirmSelectorSelection" :disabled="selectorSelected.length === 0">
          确认选择
        </el-button>
      </div>
    </el-dialog>

    <el-dialog
      v-model="detailDialogVisible"
      title="试卷详情"
      width="800px"
    >
      <el-descriptions :column="2" border v-if="currentPaper">
        <el-descriptions-item label="试卷标题">{{ currentPaper.title }}</el-descriptions-item>
        <el-descriptions-item label="所属课程">{{ currentPaper.courseName || '未关联课程' }}</el-descriptions-item>
        <el-descriptions-item label="总分">{{ currentPaper.totalScore }} 分</el-descriptions-item>
        <el-descriptions-item label="题目数">{{ currentPaper.questionCount }} 题</el-descriptions-item>
        <el-descriptions-item label="考试时长">{{ currentPaper.duration }} 分钟</el-descriptions-item>
        <el-descriptions-item label="组卷方式">
          <el-tag :type="currentPaper.creationMethod === 'AUTO' ? 'primary' : 'success'">
            {{ currentPaper.creationMethodName }}
          </el-tag>
        </el-descriptions-item>
        <el-descriptions-item label="状态">
          <el-tag :type="getStatusType(currentPaper.status)">
            {{ currentPaper.statusName }}
          </el-tag>
        </el-descriptions-item>
        <el-descriptions-item label="创建时间">{{ currentPaper.createdAt }}</el-descriptions-item>
      </el-descriptions>

      <el-divider content-position="left">题目列表</el-divider>

      <el-table :data="currentPaper.questions" stripe size="small" v-if="currentPaper.questions">
        <el-table-column type="index" label="序号" width="60" />
        <el-table-column prop="typeName" label="类型" width="80" />
        <el-table-column prop="content" label="题目内容" min-width="250" show-overflow-tooltip />
        <el-table-column prop="score" label="分值" width="80" />
        <el-table-column prop="difficulty" label="难度" width="80">
          <template #default="{ row }">
            <el-tag :type="getDifficultyType(row.difficulty)" size="small">
              {{ row.difficulty || '-' }}
            </el-tag>
          </template>
        </el-table-column>
        <el-table-column label="答案" width="100" show-overflow-tooltip>
          <template #default="{ row }">
            {{ row.answer }}
          </template>
